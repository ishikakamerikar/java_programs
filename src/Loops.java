public class Loops {
    public static void main(String[] args)
    {
        int i=0,j=0,k=0;
       for(i=0;i<=10;i++) // FOR LOOP
        {
            System.out.print(i+" ");
        }
        System.out.println("\n");
        while(j<11) //DO-WHILE LOOP
        {
            System.out.print(j+" ");
            j++;
        }
        System.out.println("\n");
        do  //DO - WHILE LOOP
        {
            System.out.print(k+" ");
            k++;
        }
        while(k<11);

    }
}
